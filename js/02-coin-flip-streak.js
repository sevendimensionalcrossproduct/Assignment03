let language = prompt("Select language code: \nSpanish: es\nGerman: de\nEnglish: en\nFrench: fr");
if (language === "es"){
    console.log("Hola mundo");
} else if (language === "de"){
    console.log("Hallo Welt");
} else if (language === "fr"){
    console.log("Bonjour le monde");
} else {
    console.log("Hello world");
}

